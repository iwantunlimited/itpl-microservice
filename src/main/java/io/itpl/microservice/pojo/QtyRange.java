package io.itpl.microservice.pojo;

public class QtyRange {

	private int qtyFrom;
	private int qtyUpTo;
	private double price;
	
	public int getQtyFrom() {
		return qtyFrom;
	}
	public void setQtyFrom(int qtyFrom) {
		this.qtyFrom = qtyFrom;
	}
	public int getQtyUpTo() {
		return qtyUpTo;
	}
	public void setQtyUpTo(int qtyUpTo) {
		this.qtyUpTo = qtyUpTo;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
